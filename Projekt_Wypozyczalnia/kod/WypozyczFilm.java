    /**
     * Metoda po nacisnieciu guzika wraca do opcji klienta.
     * @param evt - nacisniecie guzika powoduje powrot do opcji klienta
     */
    private void wrocButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_wrocButtonActionPerformed
        try {
            opcje = new Opcje(nazwaKonta, wypozyczalnia);
        } catch (Exception ex) {
            UI.printErrorMessage(ex.getMessage());
            return;
        }
        opcje.setResizable(false);
        opcje.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
        opcje.setSize(800,500);
        opcje.setLocation(500,250);
        opcje.setVisible(true);
        try {
            opcje.aktualizujFilmy();
        } catch (Exception ex) {
            UI.printErrorMessage(ex.getMessage());
            return;
        }
        this.dispose();
    }//GEN-LAST:event_wrocButtonActionPerformed

